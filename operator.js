// Mendefinisikan variabel
var a = 10;
var b = 5;

// Operator aritmatika
var tambah = a + b;
var kurang = a - b;
var kali = a * b;
var bagi = a / b;
var modulo = a % b;

// Operator perbandingan
var lebihBesar = a > b;
var lebihKecil = a < b;
var samaDengan = a === b;
var tidakSama = a !== b;

// Menampilkan hasil di console
console.log("a + b = " + tambah);
console.log("a - b = " + kurang);
console.log("a * b = " + kali);
console.log("a / b = " + bagi);
console.log("a % b = " + modulo);
console.log("a > b: " + lebihBesar);
console.log("a < b: " + lebihKecil);
console.log("a === b: " + samaDengan);
console.log("a !== b: " + tidakSama);
